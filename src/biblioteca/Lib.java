    private void libroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_libroActionPerformed
        // TODO add your handling code here:
        String instruccion;
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            conexion = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Biblioteca;integratedSecurity=true;");
            JOptionPane.showMessageDialog(null, "Conectado");

            instruccion = "{call ingresarLi(?,?,?,?,?,?,?,?)}";
            PreparedStatement s = conexion.prepareStatement(instruccion);
            s.setString(1, nomdre.getText());
            s.setString(2, autor.getText());
            s.setString(3,(String) tipo.getSelectedItem());
            s.setString(4, año.getText());
            s.setString(5, fecha.getText());
            s.setString(6, reparaciones.getText());
            s.setString(7, prestamos.getText());
            s.setString(8, tema.getText());

            s.executeUpdate();

            JOptionPane.showMessageDialog(null, "Se añadio un libro");
            nomdre.setText("");
            autor.setText("");
            tipo.setSelectedItem("");
            año.setText("");
            fecha.setText("");
            reparaciones.setText("");
            prestamos.setText("");
            tema.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error" + ex);
        }
    }//GEN-LAST:event_libroActionPerformed

