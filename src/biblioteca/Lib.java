    private void consultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tabla = (DefaultTableModel) tabla1.getModel();
        tabla.addColumn("Nombre");
        tabla.addColumn("Autor");
        tabla.addColumn("Tipo");
        tabla.addColumn("Fecha de Publicaciòn");
        tabla.addColumn("Fecha de Registro");
        tabla.addColumn("Reparaciones");
        tabla.addColumn("Numero de Prestamos");
        tabla.addColumn("Motivo");

        String instruccion = "select*from libro";
        tabla.setRowCount(0);
        try {
            System.out.println("Conectado");
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            conexion = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=Biblioteca;integratedSecurity=true;");
            Statement stmt = conexion.createStatement();
            stmt.executeQuery(instruccion);
            ResultSet rs = stmt.getResultSet();
            if (rs != null) {
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString(1));
                    v.add(rs.getString(2));
                    v.add(rs.getString(3));
                    v.add(rs.getString(4));
                    v.add(rs.getString(5));
                    v.add(rs.getString(6));
                    v.add(rs.getString(7));
                    v.add(rs.getString(8));

                    tabla.addRow(v);
                }
            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_consultaActionPerformed

